package practice.servletjsp.beans;

public class QuickQuestionBean {
	private String stud_fname;
	private String stud_lname;
	private String day_phone;
	private String evening_phone;
	private String stud_email;
	private String question;
	public String getStud_fname() {
		return stud_fname;
	}
	public void setStud_fname(String stud_fname) {
		this.stud_fname = stud_fname;
	}
	public String getStud_lname() {
		return stud_lname;
	}
	public void setStud_lname(String stud_lname) {
		this.stud_lname = stud_lname;
	}
	public String getDay_phone() {
		return day_phone;
	}
	public void setDay_phone(String day_phone) {
		this.day_phone = day_phone;
	}
	public String getEvening_phone() {
		return evening_phone;
	}
	public void setEvening_phone(String evening_phone) {
		this.evening_phone = evening_phone;
	}
	public String getStud_email() {
		return stud_email;
	}
	public void setStud_email(String stud_email) {
		this.stud_email = stud_email;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	
	
}
